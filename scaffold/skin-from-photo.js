#!/usr/bin/env node
/* ------------------------------------------------------------------
   skin-from-photo.js
   Generate a complete, contrast-checked personality (skin) by
   extracting a palette from an image and pairing it with a form preset.

   Nobody picks colors: the photo decides them, color math derives the
   tints/shades, and WCAG checks guarantee legibility.

   Usage:
     node skin-from-photo.js <image> <skin-name> [--form modern|elegant|industrial|clean]

   Writes: ../personalities/<skin-name>.css
   ------------------------------------------------------------------ */
const path = require('path');
const fs = require('fs');
const Vibrant = require('node-vibrant');
const chroma = require('chroma-js');
const sharp = require('sharp');
const forms = require('./forms');

const argv = process.argv.slice(2);
const imgArg = argv[0];
const nameArg = argv[1];
if (!imgArg || !nameArg) {
  console.error('Usage: node skin-from-photo.js <image> <skin-name> [--form modern|elegant|industrial|clean]');
  process.exit(1);
}
let formKey = 'modern';
const fi = argv.indexOf('--form');
if (fi !== -1 && argv[fi + 1]) formKey = argv[fi + 1];
const form = forms[formKey] || forms.modern;

/* nudge a foreground color until it meets a WCAG ratio against bg */
function ensureContrast(fg, bg, ratio, dir) {
  let c = chroma(fg);
  let guard = 0;
  while (chroma.contrast(c, bg) < ratio && guard < 40) {
    c = dir === 'darken' ? c.darken(0.12) : c.brighten(0.12);
    guard++;
  }
  return c;
}

/* white or near-black — whichever is more legible on bg */
function pickReadable(bg) {
  return chroma.contrast(bg, '#ffffff') >= chroma.contrast(bg, '#15171b') ? '#ffffff' : '#15171b';
}

/* normalize any format (webp/avif/jpg/png/...) to a PNG buffer first,
   so the palette extractor never chokes on an unsupported MIME type */
sharp(imgArg).png().toBuffer()
  .then(buf => Vibrant.from(buf).getPalette())
  .then(p => {
  const hex = (sw, fb) => (sw && sw.hex) ? sw.hex : fb;

  const brandRaw  = hex(p.Vibrant, hex(p.DarkVibrant, '#2563eb'));
  const accentRaw = hex(p.Muted, hex(p.DarkMuted, '#64748b'));
  const darkRaw   = hex(p.DarkMuted, chroma(brandRaw).darken(2.5).hex());
  const lightRaw  = hex(p.LightMuted, hex(p.LightVibrant, '#f4f4f2'));

  /* backgrounds — near-white tinted by the image's light hue */
  const bg      = chroma(lightRaw).set('hsl.l', 0.97).desaturate(0.4);
  const bgAlt   = bg.darken(0.45);
  let   bgDeep  = chroma(darkRaw);
  if (bgDeep.luminance() > 0.10) bgDeep = bgDeep.set('hsl.l', 0.11);
  const surface = chroma(bg).set('hsl.l', 0.995);

  /* ink — derived from the dark anchor, very dark & nearly neutral */
  let ink = chroma(darkRaw).set('hsl.l', 0.15).desaturate(1);
  ink = ensureContrast(ink, bg, 8, 'darken');
  let inkSoft = chroma(ink).set('hsl.l', 0.42).desaturate(0.6);
  inkSoft = ensureContrast(inkSoft, bg, 4.5, 'darken');

  const line = bgAlt.darken(0.4);
  const lineStrong = line.darken(0.7);

  /* brand + guaranteed-legible text on it */
  let brand = chroma(brandRaw);
  let brandInk = pickReadable(brand);
  if (chroma.contrast(brand, brandInk) < 4.5) {
    brand = brandInk === '#ffffff' ? brand.darken(1) : brand.brighten(1);
    brandInk = pickReadable(brand);
  }
  const brandDeep = brand.darken(1.1);
  const accent = chroma(accentRaw);

  const palette = {
    '--c-bg': bg.hex(),
    '--c-bg-alt': bgAlt.hex(),
    '--c-bg-deep': bgDeep.hex(),
    '--c-surface': surface.hex(),
    '--c-ink': ink.hex(),
    '--c-ink-soft': inkSoft.hex(),
    '--c-ink-invert': '#ffffff',
    '--c-ink-invert-soft': 'rgba(255,255,255,0.72)',
    '--c-brand': brand.hex(),
    '--c-brand-deep': brandDeep.hex(),
    '--c-brand-ink': brandInk,
    '--c-accent': accent.hex(),
    '--c-line': line.hex(),
    '--c-line-strong': lineStrong.hex(),
  };

  const lines = obj => Object.entries(obj).map(([k, v]) => `  ${k}: ${v};`).join('\n');
  const cInkBg   = chroma.contrast(ink, bg).toFixed(2);
  const cBrand   = chroma.contrast(brand, brandInk).toFixed(2);
  const cInvDeep = chroma.contrast('#ffffff', bgDeep).toFixed(2);

  const css =
`/* ============================================================
   GENERATED SKIN — ${nameArg}
   Palette from: ${path.basename(imgArg)}   ·   Form: ${formKey}
   WCAG: ink/bg ${cInkBg}:1 · brand-ink/brand ${cBrand}:1 · invert/deep ${cInvDeep}:1
   Generated by skin-from-photo.js — regenerate, don't hand-edit.
   ============================================================ */
@import url('${form.fontImport}');

:root {
  /* ---- palette (extracted from photo) ---- */
${lines(palette)}

  /* ---- form preset: ${formKey} ---- */
${lines(form.tokens)}
}
${form.flourishes ? '\n' + form.flourishes + '\n' : ''}`;

  const outPath = path.join(__dirname, '..', 'personalities', nameArg + '.css');
  fs.writeFileSync(outPath, css, 'utf8');

  console.log('✓ wrote ' + path.relative(process.cwd(), outPath));
  console.log('  palette:', JSON.stringify(palette));
  console.log(`  contrast  ink/bg ${cInkBg}  brand-ink/brand ${cBrand}  invert/deep ${cInvDeep}`);
}).catch(err => { console.error('Failed:', err.message); process.exit(1); });
